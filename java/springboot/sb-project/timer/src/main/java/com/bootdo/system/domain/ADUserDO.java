package com.bootdo.system.domain;

import java.io.Serializable;
import java.util.Date;

public class ADUserDO implements Serializable {
    private static final long serialVersionUID = 1L;
    // 用户名
    private String fusername;
    // 姓名
    private String sn;
    // 用户真实姓名
    private String displayname;
    // 密码
    private String fguid;
    // 部门
    private String fdepartment;
    // 公司
    private String fcompany;
    // 职位
    private String ftitle;
    // 邮箱
    private String femailaddress;
    // 手机号
    private String telephonenumber;
    // 创建用户id
    private Long userIdCreate;
    // 创建时间
    private Date fcreatetime;
    // 工号
    private String fgivenname;

    public String getFusername() {
        return fusername;
    }

    public void setFusername(String fusername) {
        this.fusername = fusername;
    }

    public String getSn() {
        return sn;
    }

    public void setSn(String sn) {
        this.sn = sn;
    }

    public String getDisplayname() {
        return displayname;
    }

    public void setDisplayname(String displayname) {
        this.displayname = displayname;
    }

    public String getFguid() {
        return fguid;
    }

    public void setFguid(String fguid) {
        this.fguid = fguid;
    }

    public String getFdepartment() {
        return fdepartment;
    }

    public void setFdepartment(String fdepartment) {
        this.fdepartment = fdepartment;
    }

    public String getFcompany() {
        return fcompany;
    }

    public void setFcompany(String fcompany) {
        this.fcompany = fcompany;
    }

    public String getFtitle() {
        return ftitle;
    }

    public void setFtitle(String ftitle) {
        this.ftitle = ftitle;
    }

    public String getFemailaddress() {
        return femailaddress;
    }

    public void setFemailaddress(String femailaddress) {
        this.femailaddress = femailaddress;
    }

    public String getTelephonenumber() {
        return telephonenumber;
    }

    public void setTelephonenumber(String telephonenumber) {
        this.telephonenumber = telephonenumber;
    }

    public Long getUserIdCreate() {
        return userIdCreate;
    }

    public void setUserIdCreate(Long userIdCreate) {
        this.userIdCreate = userIdCreate;
    }

    public Date getFcreatetime() {
        return fcreatetime;
    }

    public void setFcreatetime(Date fcreatetime) {
        this.fcreatetime = fcreatetime;
    }

    public String getFgivenname() {
        return fgivenname;
    }

    public void setFgivenname(String fgivenname) {
        this.fgivenname = fgivenname;
    }

    @Override
    public String toString() {
        return "ADUserDO [fusername=" + fusername + ", sn=" + sn + ", displayname=" + displayname + ", fguid=" + fguid
                + ", fdepartment=" + fdepartment + ", fcompany=" + fcompany + ", ftitle=" + ftitle + ", femailaddress="
                + femailaddress + ", telephonenumber=" + telephonenumber + ", userIdCreate=" + userIdCreate
                + ", fcreatetime=" + fcreatetime + ", fgivenname=" + fgivenname + "]";
    }


}
